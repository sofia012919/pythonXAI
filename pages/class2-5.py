L5 = [1, 2, 3, "蘋果", True, 3.14]
Ｌ6 = [1, 2, 3, ["蘋果", "香蕉"]]

print(L6[1])
print(L6[3][0])

L = [1, 2, 3, "a", "b", "c"]
print(L[1:4:2])
print(L[1:4])
print(L[::2])  # 每隔一個取一次
