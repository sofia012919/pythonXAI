# 什麼是串列（list）？
#
# 串列（list）就像是一個可以裝很多東西的「大盒子」，
# 裡面可以放數字、文字，甚至是其他串列。
# 每個東西都有自己的位置（從0開始算）。
#
# 例如：
my_list = [10, 20, 30, 40, 50]
# 這個串列有5個數字，分別在不同的位置。
#
# 你可以這樣拿出裡面的東西：
print(my_list[0])  # 會印出 10（第一個）
print(my_list[2])  # 會印出 30（第三個）
#
# 你也可以加東西進去：
my_list.append(60)  # 在最後加上 60
print(my_list)  # 會印出 [10, 20, 30, 40, 50, 60]
#
# 串列很方便，可以一次存很多資料，也能用 for 迴圈一個一個拿出來用！
L1 = []
L2 = ["蘋果"]
Ｌ3 = ["蘋果,香蕉,橘子"]
